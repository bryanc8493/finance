package domain.beans;

public class Salary {

	private int grade;
	private int minPay;
	private int midPay;
	private int maxPay;
	private int stiTarget;
	private int stiMax;
	private int mtiTarget;
	private int mtiMax;

	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

	public int getMinPay() {
		return minPay;
	}

	public void setMinPay(int minPay) {
		this.minPay = minPay;
	}

	public int getMidPay() {
		return midPay;
	}

	public void setMidPay(int midPay) {
		this.midPay = midPay;
	}

	public int getMaxPay() {
		return maxPay;
	}

	public void setMaxPay(int maxPay) {
		this.maxPay = maxPay;
	}

	public int getStiTarget() {
		return stiTarget;
	}

	public void setStiTarget(int stiTarget) {
		this.stiTarget = stiTarget;
	}

	public int getStiMax() {
		return stiMax;
	}

	public void setStiMax(int stiMax) {
		this.stiMax = stiMax;
	}

	public int getMtiTarget() {
		return mtiTarget;
	}

	public void setMtiTarget(int mtiTarget) {
		this.mtiTarget = mtiTarget;
	}

	public int getMtiMax() {
		return mtiMax;
	}

	public void setMtiMax(int mtiMax) {
		this.mtiMax = mtiMax;
	}
}
